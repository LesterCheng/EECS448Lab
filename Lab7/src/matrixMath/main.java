package matrixMath;

public class main {

	public static void main(String[] args) {

	}
	
	//Function to print contents of an input array
	public static void printArr(double[][] arr) {
		for(int i = 0; i < arr.length; i++) {
			for(int j = 0; j < arr.length; j++) {
				System.out.print(arr[i][j] + " ");
			}
			System.out.println("");
		}
	}


}

